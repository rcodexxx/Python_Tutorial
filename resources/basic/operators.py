# Python Operators

# You can use arithmetic operators on numbers (like integers or floats) to perform mathematical tasks

x = 15
y = 4

# addition
print(x + y)
# substraction
print(x - y)
# Multiplication
print(x * y)
# Division
print(x / y)
# Modulus
print(x % y)
# Floor division
print(x // y)
# Exponentiation
print(x ** y)