# Python Variables

# Variables are created automatically on assignment.
x = 5
y = 0.2
print(x)
print(y)


# A variable's type is not fixed and can be changed later.
n = 2
n = "Rory"
print(n)

# Use the type() function to check a variable's data type.
a = 5
b = "Hello"
print(type(a), type(b))

# Strings can be defined with either single ('') or double ("") quotes.
m = "Hello"
# Both are equivalent.
m = 'Hello'
